package com.example.spoppin;

public class BarRank {
	public int icon;
	public int spops;
	public int rank;
	public String name;
	public BarRank(){
		super();
	}
	public BarRank(int icon, String name, int spops, int rank){
		super();
		this.icon = icon;
		this.spops = spops;
		this.name = name;
		this.rank = rank;
	}
}
